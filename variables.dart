void main() {
  // Variable declaration with type
  String name = 'Mobilei';
  
  // Using var
  var age = 25;

  // Using final (cannot be changed)
  final country = 'Kenya';

  // Using const (compile-time constant)
  const pi = 3.1415;

  // Print statements
  print('Name: $name');
  print('Age: $age');
  print('Country: $country');
  print('PI: $pi');
}
